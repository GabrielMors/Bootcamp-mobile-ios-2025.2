// Cinco princípios de design de software orientado a objetos para criar código mais legível e de fácil manutenção.

// S - Single of responsability
// Cada classe/struct deve ter um unico motivo para mudar, ou seja, uma unica responsabilidade

// O - Open/Closed Principle
// Uma classe/struct deve estar aberta para extensao mas fechada para modificacao

// L - Liskov Substituion Principle
// Se B herda de A, voce deve poder usar B no lugar de A sem quebrar nada
// ou seja, a subclasse nao pode "estragar" o contrato da classe

// I - Interface Segregation
// É melhor ter varios protocolos pequenos, especificas, do que um protocolo gigante que força a classe a implementar o que nao usam

// D - Dependency Inversion Principle
// Em vez de uma classe depender diretamente de uma implementacao concreta, ela depende de protocolos
